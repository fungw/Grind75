/**
 * @param {character[][]} grid
 * @return {number}
 */
var numIslands = function(grid) {
    const queue = [];
    const adjacent = [-1, 0, 1, 0, -1];
    let i = 0, j = 0;
    let numberOfIslands = 0;
    const ISLAND = '1';
    const WATER = '0';

    while (i < grid.length) {
        while (j < grid[i].length) {
            const current = grid[i][j];
            if (current === ISLAND) {
                queue.push([i, j]);
                while (queue.length > 0) {
                    const [searchI, searchJ] = queue.shift();
                    grid[searchI][searchJ] = WATER;
                    
                    for (let k=0; k<=adjacent.length-2; k++) {
                        const currI = adjacent[k] + searchI;
                        const currJ = adjacent[k+1] + searchJ;
                        if (currI >= 0 && currI < grid.length
                            && currJ >= 0 && currJ < grid[i].length
                                && grid[currI][currJ] === ISLAND) {
                            queue.push([currI, currJ]);
                        }
                    }
                }

                numberOfIslands++;
            }
            j++;
        }
        j = 0;
        i++;
    }

    return numberOfIslands;
};

/*
- BFS with queue
- Hardcoded array for traversal - [-1, 0, 1, 0, 1];
- Start at [0, 0]. Check if 1. If 1, push onto queue. Traverse.
- Shift the queue, mark as 0 (visited), and push adjacent nodes of 1 to queue, repeat.
- When queue is empty, increase counter for number of islands
- Continue with traversal.
- N.B: Make sure to mark the cell as visited as soon as it gets onto queue to prevent efficiency issues
*/

